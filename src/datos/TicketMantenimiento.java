package datos;

import java.util.GregorianCalendar;

public class TicketMantenimiento {
	private int idTicketMantenimiento;
	private String problema;
	private GregorianCalendar fechaInicio;
	private float precio;
	private GregorianCalendar fechaResolucion;
	private boolean terminado;
	private Login login;
	private TipoMantenimiento tipoMantenimiento;
	
	 public TicketMantenimiento(){
		 
	 }
	public TicketMantenimiento (String problema, GregorianCalendar fechaInicio, float precio,
			GregorianCalendar fechaResolucion, boolean terminado, Login login,
			TipoMantenimiento tipoMantenimiento) {
		
		this.problema = problema;
		this.fechaInicio = fechaInicio;
		this.precio = precio;
		this.fechaResolucion = fechaResolucion;
		this.terminado = terminado;
		this.login = login;
		this.tipoMantenimiento = tipoMantenimiento;
	}
	public int getIdTicketMantenimiento() {
		return idTicketMantenimiento;
	}
	protected void setIdTicketMantenimiento(int idTicketMantenimiento) {
		this.idTicketMantenimiento = idTicketMantenimiento;
	}
	public String getProblema() {
		return problema;
	}
	public void setProblema(String problema) {
		this.problema = problema;
	}
	public GregorianCalendar getFechaInicio() {
		return fechaInicio;
	}
	public void setFechaInicio(GregorianCalendar fechaInicio) {
		this.fechaInicio = fechaInicio;
	}
	public float getPrecio() {
		return precio;
	}
	public void setPrecio(float precio) {
		this.precio = precio;
	}
	public GregorianCalendar getFechaResolucion() {
		return fechaResolucion;
	}
	public void setFechaResolucion(GregorianCalendar fechaResolucion) {
		this.fechaResolucion = fechaResolucion;
	}
	public boolean isTerminado() {
		return terminado;
	}
	public void setTerminado(boolean terminado) {
		this.terminado = terminado;
	}
	public Login getLogin() {
		return login;
	}
	public void setLogin(Login login) {
		this.login = login;
	}
	public TipoMantenimiento getTipoMantenimiento() {
		return tipoMantenimiento;
	}
	public void setTipoMantenimiento(TipoMantenimiento tipoMantenimiento) {
		this.tipoMantenimiento = tipoMantenimiento;
	}
	
	@Override
	public String toString() {
		return "TicketMantenimiento [idTicketMantenimiento=" + idTicketMantenimiento + ", problema=" + problema
				+ ", fechaInicio=" + fechaInicio + ", precio=" + precio + ", fechaResolucion=" + fechaResolucion
				+ ", terminado=" + terminado +  "]";
	}
	
	
	
}
