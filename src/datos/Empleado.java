package datos;

public class Empleado {
	private int idEmpleado;
	private String nombre;
	private String apellido;
	private long dni;
	private String telefono;
	private String direccion;
	private String email;
	private String nroLegajo;
	private float sueldo;
	private TipoEmpleado tipoEmpleados;
	private Login login;//ver

	public Empleado() {
	}
	
	public Empleado(String nombre, String apellido, String telefono, String direccion, long dni, String email,
			String nroLegajo, float sueldo,TipoEmpleado tipoEmpleados) {
		this.nombre = nombre;
		this.apellido = apellido;
		this.telefono = telefono;
		this.direccion = direccion;
		this.dni = dni;
		this.email = email;
		this.nroLegajo = nroLegajo;
		this.sueldo = sueldo;
		this.tipoEmpleados=tipoEmpleados;
	}


	public Empleado(String nombre, String apellido, String telefono, String direccion, long dni, String email,
			String nroLegajo, float sueldo,TipoEmpleado tipoEmpleados, Login login) {
		this.nombre = nombre;
		this.apellido = apellido;
		this.telefono = telefono;
		this.direccion = direccion;
		this.dni = dni;
		this.email = email;
		this.nroLegajo = nroLegajo;
		this.sueldo = sueldo;
		this.tipoEmpleados=tipoEmpleados;
		this.login=login;
	}

	public Login getLogin() {
		return login;
	}

	public void setLogin(Login login) {
		this.login = login;
	}

	public int getIdEmpleado() {
		return idEmpleado;
	}

	protected void setIdEmpleado(int idEmpleado) {
		this.idEmpleado = idEmpleado;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getApellido() {
		return apellido;
	}

	public void setApellido(String apellido) {
		this.apellido = apellido;
	}

	public String getTelefono() {
		return telefono;
	}

	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}

	public String getDireccion() {
		return direccion;
	}

	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}

	public long getDni() {
		return dni;
	}

	public void setDni(long dni) {
		this.dni = dni;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getNroLegajo() {
		return nroLegajo;
	}

	public void setNroLegajo(String nroLegajo) {
		this.nroLegajo = nroLegajo;
	}

	public float getSueldo() {
		return sueldo;
	}

	public void setSueldo(float sueldo) {
		this.sueldo = sueldo;
	}

	public TipoEmpleado getTipoEmpleados() {
		return tipoEmpleados;
	}

	public void setTipoEmpleados(TipoEmpleado tipoEmpleados) {
		this.tipoEmpleados = tipoEmpleados;
	}

	
	@Override
	public String toString() {
		return "Empleado [idEmpleado=" + idEmpleado + ", nombre=" + nombre + ", apellido=" + apellido + ", telefono="
				+ telefono + ", direccion=" + direccion + ", dni=" + dni + ", email=" + email + ", nroLegajo="
				+ nroLegajo + ", sueldo=" + sueldo + "]";
	}

}
