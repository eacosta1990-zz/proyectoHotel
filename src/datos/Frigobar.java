package datos;

import java.util.Set;

public class Frigobar {
	private int idFrigobar;
	private String frigobar;
	private Set<FrigobarItem> frigobarItem;
	private Set<Estadia> estadias;
	
	
	public Frigobar() {}


	public Frigobar(String frigobar) {
		super();
		this.frigobar = frigobar;
	}


	public int getIdFrigobar() {
		return idFrigobar;
	}


	public void setIdFrigobar(int idFrigobar) {
		this.idFrigobar = idFrigobar;
	}


	public String getFrigobar() {
		return frigobar;
	}


	public void setFrigobar(String frigobar) {
		this.frigobar = frigobar;
	}


	public Set<FrigobarItem> getFrigobarItem() {
		return frigobarItem;
	}


	public void setFrigobarItem(Set<FrigobarItem> frigobarItem) {
		this.frigobarItem = frigobarItem;
	}


	public Set<Estadia> getEstadias() {
		return estadias;
	}


	public void setEstadias(Set<Estadia> estadias) {
		this.estadias = estadias;
	}


	@Override
	public String toString() {
		return "Frigobar [idFrigobar=" + idFrigobar + ", frigobar=" + frigobar + ", frigobarItem=" + frigobarItem
				+ ", estadias=" + estadias + "]";
	}

	
}
